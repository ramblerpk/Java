public class for_loop {
    public static void main(String[] args) {
        for (int i=1;i<=10;i++) {
            System.out.println(i);
        }
        for (int n=0;n<=5;n++) {
            System.out.println(2*n+1);
            for (int a= 10;a>0;a--) {
                System.out.println(a);
            }
        }
    }
}
